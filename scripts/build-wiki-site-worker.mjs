import { mkdir, readdir, readFile, stat, writeFile } from "node:fs/promises";
import path from "node:path";

const [siteDirArg, outputArg] = process.argv.slice(2);

if (!siteDirArg || !outputArg) {
  console.error("usage: node scripts/build-wiki-site-worker.mjs <site-dir> <output-worker>");
  process.exit(2);
}

const siteDir = path.resolve(siteDirArg);
const outputPath = path.resolve(outputArg);

const CONTENT_TYPES = new Map([
  [".css", "text/css; charset=utf-8"],
  [".html", "text/html; charset=utf-8"],
  [".js", "application/javascript; charset=utf-8"],
  [".json", "application/json; charset=utf-8"],
  [".svg", "image/svg+xml"],
  [".png", "image/png"],
  [".jpg", "image/jpeg"],
  [".jpeg", "image/jpeg"],
  [".webp", "image/webp"],
  [".ico", "image/x-icon"],
  [".txt", "text/plain; charset=utf-8"],
]);

async function walkFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await walkFiles(fullPath)));
    } else if (entry.isFile()) {
      files.push(fullPath);
    }
  }
  return files;
}

function routeFor(filePath) {
  const rel = path.relative(siteDir, filePath).split(path.sep).join("/");
  return `/${rel}`;
}

function contentTypeFor(filePath) {
  return CONTENT_TYPES.get(path.extname(filePath).toLowerCase()) || "application/octet-stream";
}

const siteStats = await stat(siteDir).catch(() => null);
if (!siteStats?.isDirectory()) {
  console.error(`site directory does not exist: ${siteDir}`);
  process.exit(1);
}

const files = (await walkFiles(siteDir)).sort((a, b) => routeFor(a).localeCompare(routeFor(b)));
const assets = [];
let totalBytes = 0;

for (const file of files) {
  const bytes = await readFile(file);
  totalBytes += bytes.length;
  assets.push([
    routeFor(file),
    {
      contentType: contentTypeFor(file),
      body: bytes.toString("base64"),
      size: bytes.length,
    },
  ]);
}

if (!assets.some(([route]) => route === "/index.html")) {
  console.error("site build did not include /index.html");
  process.exit(1);
}

const workerSource = `// Generated by scripts/build-wiki-site-worker.mjs. Do not edit by hand.
const ASSETS = new Map(${JSON.stringify(assets)});

function bytesFromBase64(base64) {
  const binary = atob(base64);
  const bytes = new Uint8Array(binary.length);
  for (let index = 0; index < binary.length; index += 1) {
    bytes[index] = binary.charCodeAt(index);
  }
  return bytes;
}

function candidatePaths(pathname) {
  const cleanPath = pathname.replace(/\\/+/g, "/");
  const paths = [];
  if (cleanPath === "/" || cleanPath === "") {
    paths.push("/index.html");
  } else {
    paths.push(cleanPath);
    if (cleanPath.endsWith("/")) {
      paths.push(cleanPath + "index.html");
    } else if (!cleanPath.split("/").pop().includes(".")) {
      paths.push(cleanPath + ".html");
      paths.push(cleanPath + "/index.html");
    }
  }
  return paths;
}

function findAsset(pathname) {
  for (const candidate of candidatePaths(pathname)) {
    const asset = ASSETS.get(candidate);
    if (asset) return asset;
  }
  return null;
}

function headersFor(asset) {
  const cacheControl = asset.contentType.startsWith("text/html")
    ? "no-cache"
    : "public, max-age=3600";
  return {
    "cache-control": cacheControl,
    "content-length": String(asset.size),
    "content-type": asset.contentType,
    "x-content-type-options": "nosniff",
  };
}

export default {
  async fetch(request) {
    if (request.method !== "GET" && request.method !== "HEAD") {
      return new Response("Method not allowed", {
        status: 405,
        headers: {
          allow: "GET, HEAD",
          "content-type": "text/plain; charset=utf-8",
        },
      });
    }

    const url = new URL(request.url);
    const asset = findAsset(url.pathname);
    if (!asset) {
      return new Response("Not found", {
        status: 404,
        headers: {
          "cache-control": "no-cache",
          "content-type": "text/plain; charset=utf-8",
          "x-content-type-options": "nosniff",
        },
      });
    }

    const body = request.method === "HEAD" ? null : bytesFromBase64(asset.body);
    return new Response(body, {
      status: 200,
      headers: headersFor(asset),
    });
  },
};
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, workerSource, "utf8");

console.log(`Embedded ${assets.length} site files (${totalBytes} bytes) in ${outputPath}`);
